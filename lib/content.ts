export const toggleCheck = (activeTitle: string, id: number): void => {
    const category = content[activeTitle as keyof typeof content];
    const updatedContent = category.map((item) =>
        item.id === id ? { ...item, isCheck: !item.isCheck } : item
    );
    content[activeTitle as keyof typeof content] = updatedContent;
};

export const countCheckedItems = (activeTitle: string): number => {
    const category = content[activeTitle as keyof typeof content];
    const sum =  category.reduce((count, item) => (item.isCheck ? count + 1 : count), 0);
    return sum;
};
